S SEGMENT PARA PUBLIC 'CODE'
ASSUME CS: S
ORG 1000H

START:
	MOV AL, 80H
	OUT 1FH, AL
	
	MOV AL, 0FFH
	OUT 19H, AL ; SEGMENT OFF
	
	MOV BL, 00H 
	
	
;new code starts here
	MOV SI, OFFSET DATA
L1:	MOV AL, BYTE PTR CS:[SI]
	OUT 1BH, AL
	
;DELAY
	MOV CX, 0FFFFH
LP1: 	LOOP LP1
	MOV CX, 0FFFFH
LP2: 	LOOP LP2
	MOV CX, 0FFFFH
LP3: 	LOOP LP3
	
	INC SI
	INC BL
	CMP BL,04H
	JE BREAKLOOP
	JMP L1
DATA:
	DB 0AH ;R2+G ON
	DB 08H ;G OFF
	DB 0CH ;Y ON
	DB 04H ;R2 OFF
BREAKLOOP:
S ENDS
END START
