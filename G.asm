	INC SI
	MOV AL, BYTE PTR CS:[SI]  
	
	
	;LED 
	OUT 1BH, AL
	
	INC SI
	INC BL
	CMP BL, 12H
	JE L0
	CMP BH, 00H
	JE LL	
	JMP L1
