S SEGMENT PARA PUBLIC 'CODE'
ASSUME CS: S
ORG 1000H

START:
	MOV AL, 80H
	OUT 1FH, AL
	
	XOR BX, BX
	MOV	BL, 3 
	
	
;new code starts here
	MOV SI, OFFSET DATA
L1:	MOV AL, BYTE PTR CS:[SI]
	OUT 19H, AL
	
;DELAY
	MOV CX, 0FFFFH
LP1: 	LOOP LP1
	
	INC SI
	DEC BL
	CMP BL,0
	JE BREAKLOOP
	JMP L1
DATA:
	DB 0C6H
	DB 0B0H
	DB 0C7H
BREAKLOOP:
S ENDS
END START
